import React, { useEffect, useState } from "react";
import "slick-carousel/slick/slick.css";
import "slick-carousel/slick/slick-theme.css";
import Slider from "react-slick";
import axios from "axios";

import Cards from "./Cards";

function Freebook() {
  const [book, setBook] = useState([]);
  useEffect(() => {
    const getBook = async () => {
      try {
        const res = await axios.get("http://localhost:4050/book");
        console.log(res.data);
        const data = res.data.filter((data) => data.category === "Free");
        console.log(data);
        setBook(data);
      } catch (error) {
        console.log(error);
      }
    };
    getBook();
  }, []);

  var settings = {
    dots: true,
    infinite: false,
    speed: 500,
    slidesToShow: 5,
    slidesToScroll: 5,
    initialSlide: 0,
    responsive: [
      {
        breakpoint: 1024,
        settings: {
          slidesToShow: 5,
          slidesToScroll: 5,
          infinite: true,
          dots: true,
        },
      },
      {
        breakpoint: 600,
        settings: {
          slidesToShow: 2,
          slidesToScroll: 2,
          initialSlide: 2,
        },
      },
      {
        breakpoint: 480,
        settings: {
          slidesToShow: 1,
          slidesToScroll: 1,
        },
      },
    ],
  };

  return (
    <>
      <div className="w-full my-7 md:px-20 px-4 ">
        <div>
          <h1 className="font-semibold text-xl pb-2">Free offered books</h1>
          <p className="text-gray-700 dark:text-gray-300 mt-4">
            Explore our range of free courses designed to enhance your skills
            and knowledge. Whether you're interested in technology, literature,
            or personal development, our expert-curated courses provide valuable
            insights and practical learning experiences. With interactive
            content and a supportive community, you can track your progress and
            connect with other learners. Start your journey today and unlock new
            opportunities for growth with our top-notch free courses. Discover
            the perfect course for you and take the next step in your learning
            adventure.
          </p>{" "}
        </div>
        <div>
          <Slider {...settings}>
            {book.map((item) => (
              <Cards item={item} key={item.id} />
            ))}
          </Slider>
        </div>
      </div>
    </>
  );
}

export default Freebook;
